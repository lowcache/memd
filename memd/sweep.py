def detect_new_projects(cfg):
    """Find git-root projects with recent claude sessions but no registration."""
    import glob as _glob
    found = set()
    cutoff = time.time() - 30 * 86400
    for jl in _glob.glob(os.path.join(CLAUDE_PROJECTS_DIR, "*", "*.jsonl")):
        try:
            if os.path.getmtime(jl) < cutoff:
                continue
            with open(jl) as f:
                for line in f:
                    try:
                        cwd = json.loads(line).get("cwd")
                    except json.JSONDecodeError:
                        continue
                    if cwd:
                        found.add(os.path.realpath(cwd))
                        break
        except OSError:
            continue
    new = []
    for cwd in found:
        if not os.path.isdir(cwd) or cwd in cfg["exclude"]:
            continue
        top = git_toplevel(cwd)
        if not top or top in cfg["exclude"] or top == HOME:
            continue
        if find_project(cfg, top) is None:
            new.append(top)
    return sorted(set(new))


def _sweep_one(cfg, path, entry, cursors, quiet, now):
    """Sweep a single project. Returns sync_project's True/None/False when a
    distill was attempted, or "idle" when there was nothing pending (budget
    enforcement only)."""
    pending = False
    for src in transcript_files(cfg, path):
        try:
            if source_pending(src, cursors) \
                    and now - os.path.getmtime(src) > quiet:
                pending = True
                break
        except OSError:
            continue
    if not pending and collect_inbox(path)[0]:
        pending = True
    if pending:
        return sync_project(cfg, path, trigger="sweep")
    enforce_budget_mistakes(path, entry["name"], cfg["budgets"]["mistakes.md"])
    return "idle"


def cmd_sweep(cfg, jobs=None):
    """Timer entry. Returns the number of failed distills (0 = clean sweep).

    Projects are swept in a thread pool (`--jobs` / config `sweep_jobs`,
    default 4) — the work is subprocess/IO bound, so threads suffice."""
    failed = 0
    ensure_global(cfg)
    if cfg["auto_scaffold"]:
        for path in detect_new_projects(cfg):
            scaffold(path)
            register(cfg, path)
            log(f"sweep: auto-scaffolded + registered {path}")
    cursors = load_json(CURSORS_PATH, {})
    quiet = cfg["quiet_seconds"]
    now = time.time()
    projects = [(p, e) for p, e in sorted(cfg["projects"].items())
                if os.path.isdir(p)]
    jobs = max(1, jobs or cfg.get("sweep_jobs", 4))
    # Parallelism is safe across DIFFERENT projects only, and that is all the
    # pool does: sync_project() takes a per-project flock (a same-project
    # overlap no-ops as "locked"), and the shared cursor/meta registries are
    # merged under the state lock by update_json().
    done, total = 0, len(projects)
    with concurrent.futures.ThreadPoolExecutor(max_workers=jobs) as pool:
        futures = {pool.submit(_sweep_one, cfg, p, e, cursors, quiet, now):
                   (p, e) for p, e in projects}
        for fut in concurrent.futures.as_completed(futures):
            path, entry = futures[fut]
            done += 1
            try:
                res = fut.result()
            except Exception as e:  # a worker crash must not kill the sweep
                log(f"sweep worker failed for {path}: {e}")
                failed += 1
                print(f"[{done}/{total}] syncing {entry['name']}... FAILED ({e})",
                      file=sys.stderr)
                continue
            if res is False:
                failed += 1
            outcome = {True: "done (distilled)", False: "FAILED distill",
                       None: "done (nothing new)"}.get(res, "done (idle)")
            print(f"[{done}/{total}] syncing {entry['name']}... {outcome}",
                  file=sys.stderr)
    # Refresh the generated project index in the global store (commit only if it
    # actually changed, to avoid churn on idle sweeps).
    if write_global_index(cfg):
        git_commit_memory(cfg.get("global_root"), "memd: refresh project index")
    log(f"sweep complete ({failed} failed distill(s))" if failed else "sweep complete")
    return failed
